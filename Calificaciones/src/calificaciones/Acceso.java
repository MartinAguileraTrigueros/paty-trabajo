    private boolean inicioDeSesion(String numeroControl,String contrasena) {
       try (BufferedReader lector=new BufferedReader(new FileReader("src/Archivos/usuarios.txt"))) {
            String linea;
            while((linea=lector.readLine())!=null) {
                String[] arr= linea.split(",");
                if (arr.length == 2) {
                    if (numeroControl.equals(arr[0].trim()) && contrasena.equals(arr[1].trim())) {
                        return true;
                    }
                }
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Archivo de usuarios corrupto");
        }
        return false;
    }

